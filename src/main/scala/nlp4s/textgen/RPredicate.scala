trait RhetoricalPredicate

object RhetoricalPredicate {
  case object Attributive extends RhetoricalPredicate
  case object Equivalent extends RhetoricalPredicate
  case object Specification extends RhetoricalPredicate
  case object Explanation extends RhetoricalPredicate
  case object Evidence extends RhetoricalPredicate
  case object Analogy extends RhetoricalPredicate
  case object Representative extends RhetoricalPredicate
  case object Constituency extends RhetoricalPredicate
  case object Covariance extends RhetoricalPredicate
  case object Alternatives extends RhetoricalPredicate
  case object CauseEffect extends RhetoricalPredicate
  case object Adversative extends RhetoricalPredicate
  case object Inference extends RhetoricalPredicate

  case object Identification extends RhetoricalPredicate
  case object Renaming extends RhetoricalPredicate
  case object Amplification extends RhetoricalPredicate
  case object Illustration extends RhetoricalPredicate
}
